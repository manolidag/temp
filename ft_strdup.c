#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*ret;

	len = ft_strlen(s1);
	ret = (char *)malloc(sizeof(*s1) * (len + 1));
	if (ret == NULL)
		return (NULL);
	return (ft_strcpy(ret, s1));
}
